package com.company;

public class Math {
    public Math() {
    }

    /**
     * Takes two ints and returns the sum
     * @param a int 1
     * @param b int 2
     * @return int 1 + int 2
     */
    public int add(int a, int b){
        return a+b;
    }

    /**
     * Takes two ints and subtracts the second from the first
     * @param a int 1
     * @param b int 2
     * @return int 1 - int 2
     */
    public int subtract(int a, int b){
        return a-b;
    }
}
